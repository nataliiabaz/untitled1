package day06_boolean;

public class UnaryExample {

    public static void main(String[] args) {
        int n=0;
        n++;
        System.out.println(n);
        ++n;
        System.out.println(n);
        System.out.println(++n);
        System.out.println(++n);
        System.out.println(n);
        System.out.println("-----------------------------");
        int x=4;
        int y=x;
        int z=++x;
        System.out.println("x"  +x);
        System.out.println("y"  +y);
        System.out.println("z"  +z);
        int candy=5;
        System.out.println(candy++);
        System.out.println(--candy);



    }








}
